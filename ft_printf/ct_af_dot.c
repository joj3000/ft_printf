#include "ft_printf.h"

int	ct_af_dot(char *s)
{
	int j;
	int count;

	j = 0;
	count = 0;
	if (is_s_dec(s) == 0)
		return (0);
	while (s[j] != '.')
		j++;
	j++;
	while (s[j])
	{
		j++;
		count++;
	}
	return (count);
}
