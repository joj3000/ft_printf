#include "ft_printf.h"

int				ct_ndec_digit(long double f)
{
	int count;

	count = 0;
	if (f > 0)
		while (f >= 1)
		{
			f /= 10;
			count++;
		}
	else if (f < 0)
		while (f <= -1)
		{
			f /= 10;
			count++;
		}
	return (count);
}
